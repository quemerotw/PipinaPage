const menuToggle = document.getElementById("menu-toggle");
const navbar = document.getElementById("navbar");

menuToggle.addEventListener("click", () => {
  if (navbar.style.left === "0px") {
    navbar.style.left = "-100%";
  } else {
    navbar.style.left = "0px";
  }
});





